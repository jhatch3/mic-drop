-- One-time Snowflake bootstrap for Pitch Battle.
-- Run as ACCOUNTADMIN (or another role with create-warehouse/database/user privs).
-- Idempotent except for CREATE USER ... PASSWORD — change the placeholder.

CREATE WAREHOUSE IF NOT EXISTS MICDROP_WH
  WITH WAREHOUSE_SIZE = 'XSMALL'
  AUTO_SUSPEND = 60
  AUTO_RESUME = TRUE
  INITIALLY_SUSPENDED = TRUE
  COMMENT = 'Pitch Battle app workloads (XS is plenty)';

CREATE DATABASE IF NOT EXISTS MICDROP;
CREATE SCHEMA IF NOT EXISTS MICDROP.PUBLIC;

CREATE ROLE IF NOT EXISTS MICDROP_APP;
GRANT USAGE ON WAREHOUSE MICDROP_WH                    TO ROLE MICDROP_APP;
GRANT USAGE ON DATABASE  MICDROP                       TO ROLE MICDROP_APP;
GRANT USAGE ON SCHEMA    MICDROP.PUBLIC                TO ROLE MICDROP_APP;
GRANT CREATE TABLE, CREATE VIEW ON SCHEMA MICDROP.PUBLIC TO ROLE MICDROP_APP;
GRANT SELECT, INSERT, UPDATE, DELETE ON FUTURE TABLES IN SCHEMA MICDROP.PUBLIC TO ROLE MICDROP_APP;
GRANT SELECT ON FUTURE VIEWS  IN SCHEMA MICDROP.PUBLIC TO ROLE MICDROP_APP;

-- Replace the placeholder password before running; share it via .env, not git.
CREATE USER IF NOT EXISTS MICDROP_APP_USER
  PASSWORD = 'drewandval4ever'
  MUST_CHANGE_PASSWORD = FALSE
  DEFAULT_ROLE = MICDROP_APP
  DEFAULT_WAREHOUSE = MICDROP_WH
  DEFAULT_NAMESPACE = MICDROP.PUBLIC
  COMMENT = 'Pitch Battle backend service account';
GRANT ROLE MICDROP_APP TO USER MICDROP_APP_USER;
